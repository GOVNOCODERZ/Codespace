def fill_list_from_input():
    """
    Запрашивает у пользователя строку вещественных чисел, разделенных пробелом,
    и возвращает их в виде списка.

    Returns:
        list: Список вещественных чисел.
    """
    while True:
        try:
            user_input = input("Введите элементы списка A (вещественные числа) через пробел: ")
            # метод split() разделяет строку по пробелам, а map() применяет функцию float к каждому элементу.
            A = list(map(float, user_input.split()))
            return A
        except ValueError:
            print("Ошибка: Пожалуйста, вводите только числа. Попробуйте еще раз.")

def process_list(A):
    """
    Обрабатывает список A и формирует новый список B[3] по следующим правилам:
    - B[0] = максимальный элемент списка A.
    - B[1] = среднее арифметическое положительных элементов списка A.
    - B[2] = абсолютное значение минимального элемента списка A.

    Args:
        A (list): Исходный список вещественных чисел.

    Returns:
        list: Сформированный список B.
    """
    if not A:
        # обработка случая, если исходный список пуст
        return [0, 0, 0]

    # B[0]: находим максимальный элемент с помощью встроенной функции max().
    max_val = max(A)

    # B[1]: вычисляем среднее арифметическое только положительных элементов.
    positive_elements = [num for num in A if num > 0]
    if positive_elements:
        # функция sum() находит сумму элементов, а len() — их количество.
        avg_positive = sum(positive_elements) / len(positive_elements)
    else:
        avg_positive = 0  # если положительных элементов нет, среднее равно 0.

    # B[2]: находим минимальный элемент функцией min() и берем его абсолютное значение функцией abs().
    abs_min_val = abs(min(A))

    B = [max_val, avg_positive, abs_min_val]
    return B