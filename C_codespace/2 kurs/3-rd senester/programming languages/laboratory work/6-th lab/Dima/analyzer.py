# Импортируем модуль re для использования регулярных выражений,
# что поможет нам эффективно очистить текст от знаков препинания.
import re

def process_text(text: str) -> tuple:
    """
    Обрабатывает входной текст для подсчета количества букв 'а' и длины каждого слова.

    Аргументы:
    text (str): Исходная строка текста.

    Возвращает:
    tuple: Кортеж, содержащий три списка:
           - list_a (list): Список с количеством вхождений буквы 'а' в каждое слово.
           - list_b (list): Список с длинами каждого слова.
           - words (list): Список слов из исходного текста.
    """
    # Приводим весь текст к нижнему регистру для корректного подсчета буквы 'а'
    # вне зависимости от ее первоначального регистра.
    lower_text = text.lower()

    # Удаляем из текста все знаки препинания, заменяя их на пробелы,
    # чтобы они не влияли на определение слов.
    # Регулярное выражение [^\w\s-] находит все символы, которые не являются
    # буквенно-цифровыми символами, пробелами или дефисами.
    cleaned_text = re.sub(r'[^\w\s-]', '', lower_text)

    # Разбиваем очищенный текст на отдельные слова по пробелам.
    # filter(None, ...) удаляет пустые строки, которые могли образоваться
    # из-за нескольких пробелов подряд.
    words = list(filter(None, cleaned_text.split()))

    # Инициализируем пустые списки A и B для хранения результатов.
    list_a = []  # Для количества букв 'а'
    list_b = []  # Для длин слов

    # Проходим по каждому слову в списке слов.
    for word in words:
        # Считаем количество букв 'а' в текущем слове и добавляем в список A.
        count_a = word.count('а')
        list_a.append(count_a)

        # Определяем длину текущего слова и добавляем в список B.
        length = len(word)
        list_b.append(length)

    return list_a, list_b, words