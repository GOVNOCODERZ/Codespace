#ifndef MEMORY_ALLOCATOR_H
#define MEMORY_ALLOCATOR_H

#include <cstddef>
#include <iostream>

/**
 * @brief Структура заголовка блока памяти.
 * 
 * Каждый блок (занятый или свободный) начинается с этого заголовка.
 * 
 * Заголовок хранит:
 * 
 - общий размер блока (включая сам заголовок, в байтах),
 *
 - флаг занятости,
 * 
 - указатель на следующий свободный блок (только если блок свободен).
 * 
 * Занятые блоки игнорируют поле nextFree.
 */
struct BlockHeader {
    size_t size; // Общий размер блока в байтах (включая заголовок)
    bool isAllocated; // `true` — блок занят, `false` — свободен
    BlockHeader* nextFree; // Указатель на следующий свободный блок (только для свободных блоков)
};

/**
 * @brief Класс программной модели управления оперативной памятью.
 * 
 * Эмулирует работу аллокатора кучи в фиксированном буфере.
 * Поддерживает:
 * 
 - выделение памяти по стратегии first-fit,
 * 
 - освобождение блока с автоматическим слиянием соседних свободных блоков,
 * 
 - расчёт степени внешней фрагментации.
 */
class MemoryAllocator {
public:
    /**
     * @brief Конструктор аллокатора.
     * @param heapSize Размер эмулируемой кучи в байтах (по умолчанию 64 КБ).
     * 
     * Выделяет буфер в куче процесса и инициализирует его одним свободным блоком.
     */
    explicit MemoryAllocator(size_t heapSize = 65536);

    /**
     * @brief Деструктор.
     * 
     * Освобождает память, выделенную под эмулируемую кучу.
     */
    ~MemoryAllocator();

    /**
     * @brief Выделяет блок памяти заданного размера.
     * @param userSize Требуемый размер данных (в байтах).
     * @return Указатель на выделенную память или nullptr при нехватке памяти.
     * 
     * Использует стратегию "first-fit". Размер блока выравнивается до 8 байт.
     * Если остаток после выделения достаточно велик, блок делится.
     */
    void* allocate(size_t userSize);

    /**
     * @brief Освобождает ранее выделенный блок памяти.
     * @param ptr Указатель, полученный от `allocate()`.
     * 
     * Помечает блок как свободный и добавляет в список свободных блоков.
     * Затем вызывается `rebuildFreeList()` для слияния соседних свободных блоков.
     */
    void deallocate(void* ptr);

    /**
     * @brief Выводит статистику о состоянии кучи.
     * 
     * Отображает:
     * 
     * - общий размер кучи,
     * 
     * - объём занятой и свободной памяти,
     * 
     * - количество блоков,
     * 
     * - степень внешней фрагментации.
     */
    void printStatus() const;

    /// @brief Возвращает текущую степень внешней фрагментации.
    /// @return Значение от 0.0 (нет фрагментации) до 1.0 (максимальная фрагментация).
    double getFragmentation() const;

private:
    static constexpr size_t ALIGNMENT = 8; // Выравнивание по 8 байт (требование архитектуры x86/x64)
    static constexpr size_t HEADER_SIZE = sizeof(BlockHeader); // Размер заголовка блока

    char* heapStart; // Указатель на начало эмулируемой кучи
    size_t heapSize; // Общий размер кучи в байтах
    BlockHeader* freeList; // Голова односвязного списка свободных блоков

    /**
     * @brief Округляет значение вверх до ближайшего кратного `alignment`.
     */
    size_t alignUp(size_t value, size_t alignment) const;

    /**
     * @brief Находит первый свободный блок, достаточно большой для `totalSize`.
     * @param totalSize Запрашиваемый размер с учётом заголовка и выравнивания.
     * @return Указатель на подходящий блок или `nullptr`.
     * 
     * Реализует стратегию "first-fit": проход по `freeList` до первого подходящего блока.
     * Найденный блок удаляется из `freeList` и возвращается для использования.
     */
    BlockHeader* findSuitableBlock(size_t totalSize);

    /**
     * @brief Полностью перестраивает список свободных блоков и сливает соседей.
     * 
     * Проходит по всей куче от начала до конца, собирает все свободные блоки
     * и строит новый `freeList`. Благодаря последовательному обходу,
     * соседние свободные блоки автоматически объединяются в один.
     */
    void rebuildFreeList();

    /**
     * @brief Проверяет, что указатель находится в пределах эмулируемой кучи.
     */
    bool isValidPtr(void* ptr) const;

    /**
     * @brief Преобразует указатель на данные в указатель на заголовок блока.
     */
    BlockHeader* headerFromPtr(void* ptr) const;

    /**
     * @brief Преобразует указатель на заголовок в указатель на данные (для возврата пользователю).
     */
    void* ptrFromHeader(BlockHeader* header) const;
};

#endif